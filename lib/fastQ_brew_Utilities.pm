####################################
# _prob_calc
####################################

sub _prob_calc {
    my $seq     = $_[0];
    my $lib     = $_[1];
    my $cutoff  = $_[2];

    my $len = length $seq;
    return 0 if $len == 0;  # conservative: drop empty

    my $prob_ref = ($lib && $lib eq 'illumina') ? \%illumina_prob : \%sanger_prob;

    my $sum_prob = 0;
    foreach my $ch ( split //, $seq ) {
        $sum_prob += $prob_ref->{$ch};
    }

    my $error_prob = ( sprintf "%.10f", $sum_prob ) * 100 / $len;

    if ( $error_prob > $cutoff ) {
        return 0;
    }
    else {
        return 1;
    }
}

